/**
 * Get the URL path for an icon file
 */
export function getIconPath(iconName: string | null | undefined): string | null {
  if (!iconName) return null;
  return `/icons/${iconName}`;
}

/**
 * Get a human-readable name from the icon filename
 */
export function getIconDisplayName(iconName: string): string {
  // Remove prefix and extension, replace hyphens with spaces, capitalize words
  const name = iconName
    .replace(/^reshot-icon-/, '')
    .replace(/-[A-Z0-9]+\.svg$/, '')
    .replace(/-/g, ' ');
  
  return name
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

/**
 * Search icons by keyword
 */
export function searchIcons(query: string): string[] {
  if (!query || query.trim() === '') {
    return AVAILABLE_ICONS;
  }
  
  const lowerQuery = query.toLowerCase().trim();
  return AVAILABLE_ICONS.filter(icon => {
    const displayName = getIconDisplayName(icon).toLowerCase();
    const filename = icon.toLowerCase();
    return displayName.includes(lowerQuery) || filename.includes(lowerQuery);
  });
}
